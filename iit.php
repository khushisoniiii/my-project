Getting admission to IIT is a dream of many students, but it is not a cakewalk, clearing the IIT entrance needs smart work along with hard work and it is commonly known as Joint Entrance Examination (JEE) conducted by CBSE.  After cracking the exam, students can apply for admission to the National Institute of Technology (NITs), Indian Institute of Technology (IITs), Centrally Funded Technical Institute (CFTIs).so, are you planning to join or JEE Mains, best IIT jam coaching in Delhi. Meri padhai helps you to find the best coaching institute for IIT.

The entrance exam has two levels- JEE Mains and JEE Advanced.  The former is comparatively on the lower end of the spectrum in terms of difficulty level then to the latter.

JEE Main has two papers (Paper 1, Paper2), Paper 1 is an online exam for admission in B.E/ B.Tech courses, and Paper 2 is an offline exam for admission in B.Arch and B.Planning.  Paper 1 consists of MCQ of the subjects Physics, Chemistry, and Mathematics. 

JEE Advanced is an academic examination held annually by one of seven zones IITs (IIT Roorkee, IIT Kharagpur, IIT Delhi, IIT Kanpur, IIT Mumbai, IIT Madras, IIT Guwahati) under the guidance of Joint Admission Board (JAB). JEE advanced score is the parameter to get admission in IIT other universities uses these scores for admission. The student admitted to IIT can't appear for the JEE Advanced. Indian Institute of Science Education and Research, Rajiv Gandhi Institute of Petroleum Technology, and an Indian Institute of Science also use JEE Advanced as a basis of admission.</pre>
